const logoWidth = 72

// renderLogo returns the styled logo with two-space left padding, or "" if the
// terminal is too narrow to fit it without wrapping.
func renderLogo(termWidth int) string {
	if termWidth > 0 && termWidth < logoWidth+2 {
		return ""
	}
	lines := strings.Split(asciiLogo, "\n")
	var b strings.Builder
	for _, line := range lines {
		b.WriteString("  ")
		b.WriteString(StyleTitle.Render(line))
		b.WriteByte('\n')
	}
	return b.String()
}
